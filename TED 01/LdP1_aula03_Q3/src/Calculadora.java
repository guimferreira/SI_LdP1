public class Calculadora {
    double total;

    public double somar(double x, double y) {
        total = x + y;
        return total;
    }
    public double subtracao(double x, double y) {
        total = x - y;
        return total;
    }
    public double divi(double x, double y) {
        total = x / y;
        return total;
    }
    public double multip(double x, double y) {
        total = x * y;
        return total;
    }

}
